    private void BusquedaKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_BusquedaKeyReleased
       
        Camas cama = new Camas();
       
       controlcama.Select_cama(cama,Busqueda.getText());
       
       NumCama.setText(cama.getNumeroCama());
       Estado.setText(cama.getEstado());
       Descripcion.setText(cama.getDescripcion());
       for(int i=0;i<Area.getItemCount();i++){
            try{
                    if(cama.getCodigoArea().equals(controlarea.Select_codigoarea(Area.getItemAt(i)))){
                        Area.setSelectedIndex(i);
                    }
            }catch(java.lang.NullPointerException e){}
        }
    }//GEN-LAST:event_BusquedaKeyReleased


