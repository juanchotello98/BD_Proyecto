    private void GuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_GuardarActionPerformed
       try{
            if(Codigo.getText().equals("")||Nombre.getText().equals("")||Objetivo.getText().equals("")){
                JOptionPane.showMessageDialog(this, "Faltan campos pòr llenar", "Error!", JOptionPane.ERROR_MESSAGE); 
            }else{
                if(!controlcampana.Select_codigoCampa(Codigo.getText())){
                    String codigo,fecha,encargado,objetivo,nombre;
                    codigo=Codigo.getText();
                    nombre=Nombre.getText();
                    encargado=Medicos.getSelectedItem().toString();
                    objetivo=Objetivo.getText();
                    fechacale = calendario.getDate();
                    fecha = formato.format(fechacale);

                    controlcampana.Insert_campanapreventiva(codigo, nombre, objetivo, fecha,encargado);
                }else{
                    JOptionPane.showMessageDialog(this, "Codigo para la campaña ya existe", "Error!", JOptionPane.ERROR_MESSAGE);
                }
        }
       }catch(java.lang.NullPointerException ex){
         JOptionPane.showMessageDialog(this, "escoge una fecha!", "Error!", JOptionPane.ERROR_MESSAGE);  
       }
    }//GEN-LAST:event_GuardarActionPerformed

